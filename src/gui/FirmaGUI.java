    @Override
    public void run()
    {
        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss      dd.MM.yyyy");
        int stunde = new Date().getHours();
        if (stunde >= 6 && stunde < 10)
        {
            laWillkommen.setText("Guten Morgen!");
        } else if (stunde >= 10 && stunde < 12)
        {
            laWillkommen.setText("Guten Mittag!");
        } else if (stunde >= 12 && stunde < 18)
        {
            laWillkommen.setText("Guten Nachmittag!");
        } else if (stunde >= 18 && stunde <= 24)
        {
            laWillkommen.setText("Guten Abend!");
        } else if (stunde < 6)
        {
            laWillkommen.setText("Gute Nacht!");
        }
        while (true)
        {
            try
            {
                laUhrzeit.setText(sdf.format(new Date()));
                Thread.sleep(1000);
            } catch (InterruptedException ex)
            {
            }
        }
    }
